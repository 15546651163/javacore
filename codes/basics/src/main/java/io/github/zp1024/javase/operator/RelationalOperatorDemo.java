package io.github.zp1024.javase.operator;

/**
 * 三元运算符 ?: 示例
 */
public class RelationalOperatorDemo {
    public static void main(String args[]) {
        int a = 10;
        int b = 20;
        System.out.println("a == b = " + (a == b));
        System.out.println("a != b = " + (a != b));
        System.out.println("a > b = " + (a > b));
        System.out.println("a < b = " + (a < b));
        System.out.println("b >= a = " + (b >= a));
        System.out.println("b <= a = " + (b <= a));
    }
};
